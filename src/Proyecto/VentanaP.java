    private void mostrarDatos(){
        String m[][] = new String[listaClientes.size()][5];
        Cliente a;
        for (int i = 0; i < listaClientes.size(); i++) {
            a = listaClientes.get(i);
            m[i][0] = Integer.toString(a.getId());
            m[i][1] = a.getNombre();
            m[i][2] = Integer.toString(a.getEdad());
            m[i][3] = a.getCorreo();
            m[i][4] = a.getTelefono();
        }
        jTableCliente.setModel(new javax.swing.table.DefaultTableModel(
            m,
            new String [] {
                "Id", "Nombre", "Edad", "Correo", "Teléfono"
            }
        ));
    }
