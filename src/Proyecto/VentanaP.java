    private void limpiar(){
        txtId.setText("");
        txtNombre.setText("");
        txtEdad.setText("");
        txtCorreo.setText("");
        txtTelefono.setText("");
    }
    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        try{
            int id = Integer.parseInt(txtId.getText());
            String nombre = txtNombre.getText();
            int edad = Integer.parseInt(txtEdad.getText());
            String correo = txtCorreo.getText();
            String telefono = txtTelefono.getText();
            listaClientes.add(new Cliente(id, nombre, edad, correo, telefono));
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Verifica los datos.");
        }
        limpiar();
        mostrarDatos();

    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try{
            String texto = txtBuscar.getText();
            for(int i = 0; i < jTableCliente.getRowCount(); i++){
                for(int j = 0; j < jTableCliente.getColumnCount(); j++){
                    if(jTableCliente.getModel().getValueAt(i, j).equals(texto)){
                        txtId.setText(jTableCliente.getModel().getValueAt(i, 0).toString());
                        txtNombre.setText(jTableCliente.getModel().getValueAt(i, j).toString());
                        txtEdad.setText(jTableCliente.getModel().getValueAt(i, 2).toString());
                        txtCorreo.setText(jTableCliente.getModel().getValueAt(i, 3).toString());
                        txtTelefono.setText(jTableCliente.getModel().getValueAt(i, 4).toString());
                        }
                    }
                }
            txtBuscar.setText("");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Verifica los datos.");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        try{
            String texto = txtEliminar.getText();
            Cliente a;
            for(int i = 0; i < jTableCliente.getRowCount(); i++){
                for(int j = 0; j < jTableCliente.getColumnCount(); j++){
                    if(jTableCliente.getModel().getValueAt(i, j).equals(texto)){
                        listaClientes.remove(i);
                    }
                }
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Verifica los datos.");
        }
        mostrarDatos();
        txtEliminar.setText("");
    }//GEN-LAST:event_btnEliminarActionPerformed

